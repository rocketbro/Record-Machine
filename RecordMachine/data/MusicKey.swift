import Foundation

enum MusicKey: String, CaseIterable, Codable {
    case aFlat = "Ab"
    case a = "A"
    case aSharp = "A#"
    case bFlat = "Bb"
    case b = "B"
    case c = "C"
    case cSharp = "C#"
    case dFlat = "Db"
    case d = "D"
    case dSharp = "D#"
    case eFlat = "Eb"
    case e = "E"
    case f = "F"
    case fSharp = "F#"
    case gFlat = "Gb"
    case g = "G"
    case gSharp = "G#"
    
}
